const {app, Menu, BrowserWindow} = require('electron')
const express = require('express')

const path = require('path')
const url = require('url')

const WINDOW_WIDTH = null
const WINDOW_HEIGHT = null

var w

var app_ready = false
var server_ready = false

const server = express()
const port = 3000

server.use(express.static(__dirname))
server.listen(port, function() {
  server_ready = true
  if (app_ready) open()
})

function open () {
  let bw_options = {
    width: WINDOW_WIDTH ? WINDOW_WIDTH : 412,
    height: WINDOW_HEIGHT ? WINDOW_HEIGHT : 870,
    resizable: false
  }

  w = new BrowserWindow(
    bw_options
  )

  w.setResizable(false)

  w.loadURL(url.format({
    pathname: 'localhost' + ':' + port,
    protocol: 'http:',
    slashes: true
  }))

  // w.webContents.openDevTools()

  w.on('closed', function () {
    w = null
  })
}

app.on('ready', function () {
  app_ready = true
  create_menus()
  if (server_ready) open()
})

app.on('window-all-closed', function () {
  app.quit()
})

function create_menus () {
  const template = [
    {
      label: app.getName(),
      submenu: [
        {
          role: 'quit'
        }
      ]
    },
    {
      label: 'Edit',
      submenu: [
        {role: 'undo'},
        {role: 'redo'},
        {type: 'separator'},
        {role: 'cut'},
        {role: 'copy'},
        {role: 'paste'},
        {role: 'pasteandmatchstyle'},
        {role: 'delete'},
        {role: 'selectall'}
      ]
    },
    {
      label: 'View',
      submenu: [
        {
          label: 'Open in Browser...',
          click () {
            require('electron').shell.openExternal('http://localhost:' + port)
          }
        },
        {
          type: 'separator'
        },
        {role: 'reload'},
        {role: 'forcereload'},
        {role: 'toggledevtools'},
        {type: 'separator'},
        {role: 'resetzoom'},
        {role: 'zoomin'},
        {role: 'zoomout'},
        {type: 'separator'},
        {role: 'togglefullscreen'}
      ]
    }
  ]

  const menu = Menu.buildFromTemplate(template)
  Menu.setApplicationMenu(menu)
}